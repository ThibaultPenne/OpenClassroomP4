                — Que dirais-tu d’un dessert ? demanda-t‑il.<br>
                En fait, ce n’était qu’une manœuvre sournoise pour gagner du temps, car s’il se levait il risquait d’attirer l’attention de Lara.<br>
                Lorsqu’il sentit le pied de Jen remonter le long de sa jambe sous la table, il soupira d’abattement tout en feignant le plaisir. Le cas échéant, il n’hésiterait pas à coucher avec elle si c’était le seul moyen de la faire parler. Le groupe terroriste sur lequel il enquêtait était dans la phase finale de préparation d’un gros coup. Alors qu’ils étaient prêts à passer à l’action, il n’avait toujours aucune idée de ce que c’était ni de l’endroit où ça devait se passer.<br>
                Même si s’envoyer en l’air avec Jen signifiait la fin de sa carrière, ou que la jeune femme ne puisse être poursuivie parce qu’il avait commis une faute dans le traitement de son cas, il le ferait pour sauver des vies humaines. C’était sa priorité. Et il était résolu à ne pas perdre de vue son objectif. Il était dans le business depuis trop longtemps pour s’écraser sans poser de questions, pour obéir à des règles qui allaient contre son jugement. Trop de vies avaient été perdues. Il avait pris trop de vies. En son for intérieur, il ressentait un immense besoin de se racheter. Cette fois, il ferait tout ce qu’il avait à faire. Il n’y avait aucune limite.<br>
                Si seulement Lara pouvait se lever et s’en aller…<br>
                Au lieu de cela, elle tourna la tête et croisa son regard. Elle battit des paupières, puis ses yeux s’arrondirent de stupeur et elle pâlit.<br>
                — Hé, tu sais quoi ? lança-t‑il à Jen en se levant d’un mouvement brusque. Oublions le dessert et allons dans un endroit plus intime.<br>
                Jen ramassa son sac et se leva sans se faire prier. De toute évidence, elle n’attendait que ça.<br>
                Reid laissa deux billets de vingt sur la table, assez pour payer leurs repas, le service et le pourboire. Le sourire de Jen s’élargit tandis qu’elle enfilait son manteau. <br>Quelles que soient les convictions anticapitalistes des membres de la cellule terroriste, elle ne semblait pas détester l’argent.<br>
                Lara s’était levée, elle aussi, et disait quelque chose à son compagnon. Elle paraissait plus douce, ses formes étaient plus voluptueuses que dans le souvenir de Reid. Elle se dirigea vers lui. Son élégante robe de soie noire épousait un corps qui n’avait rien à voir avec celui des mannequins anémiques et possédait tout ce qui pouvait contenter les mains d’un homme amateur de perfection.<br>
                Une fois sa veste endossée, Reid saisit le bras de Jen et la tira vers l’entrée du restaurant.<br>
                Lara hésita un bref instant, mais se ressaisit et continua à marcher dans sa direction.<br>
                Il calcula la distance jusqu’à sa voiture. Ils n’allaient pas y arriver. Le pot aux roses serait découvert à la seconde où Lara l’appellerait par son vrai prénom, Reid et non Dave.<br>
                Ils étaient devant la porte. La franchissaient. Il observa les véhicules stationnés sur toute la longueur de l’îlot.
