                La dernière chose qu’il vit avant de s’engager sur le large boulevard fut le SUV noir opérant un demi-tour, et le corps inerte de Jen baignant dans une mare de sang, illuminé par le néon du restaurant. Une image digne d’un ancien film policier.<br>
                Sauf qu’ici, il s’agissait de la vraie vie.<br>
                Il jura sourdement.<br>
                Il venait de perdre son atout le plus prometteur dans une affaire brûlante. Les dents serrées, il écrasa la pédale d’accélérateur et slaloma entre les véhicules.<br>
                — Reid ?<br>
                La voix de Lara était faible, ce qui lui était inhabituel.<br>
                Elle était assise aussi loin que possible de lui, et son expression était celle de quelqu’un qui a vu un fantôme. Ce qui, d’une certaine manière, n’était pas faux. Car selon les informations qu’elle avait dû avoir de lui, il était mort deux ans plus tôt, la nuit où il avait perdu tout contrôle avec elle, dans la boulangerie.<br>
                — Je ne comprends pas…<br>
                — Une minute.<br>
                Il ne pouvait pas se permettre d’être distrait en ce moment précis. Levant les yeux vers le rétroviseur intérieur, il jura entre ses dents.
