                Puis tout se passa à la vitesse de l’éclair.<br>
                Lara sortit du restaurant. Plus vite qu’il ne s’y attendait. Peut‑être courait‑elle. Elle serrait les bras sur sa taille sous le coup de fouet du vent.<br>
                — Reid ? Que…  ?<br>
                Sa voix se perdit dans un hurlement de pneus tandis qu’un SUV noir marquait un arrêt brutal le long du trottoir, et que deux hommes masqués — l’un sur le siège passager, l’autre à l’arrière — ouvraient le feu.<br>
                Reid plongea vers Lara, vaguement conscient du fait que Jen se jetait sur le sol derrière lui comme une pro. Enveloppant Lara de ses bras, il roula avec elle jusqu’à l’abri fourni par l’imposant panneau publicitaire du restaurant.<br>
                Une balle perfora celui-ci à trois centimètres de sa tête, lui rappelant s’il le fallait que les poubelles, portières de voitures et autres obstacles improvisés ne protégeaient des balles que dans les films. Mais au moins, grâce à lui, les tireurs ne pouvaient pas les viser avec précision.<br>
                Lors d’une accalmie dans les tirs, il risqua un œil hors de leur abri. Le SUV faisait marche arrière pour se rapprocher d’eux.<br>
                Il sauta sur ses pieds, releva Lara, et, s’élançant avec elle, les fit s’accroupir derrière la voiture la plus proche, puis la suivante, tandis que la fusillade reprenait et que les balles ricochaient autour d’eux.<br>
                Enfin, il fut près de son propre véhicule. L’instant d’après, grâce à un double de la clé caché sous l’aile avant, ils se glissèrent à l’intérieur.<br>
                <br>
                Quelques secondes plus tard, ils quittaient cet enfer, bénéficiant d’un avantage temporaire : ils se trouvaient dans le bon sens, alors que leurs ennemis devaient effectuer une marche arrière pour les prendre en chasse.<br>
            </p>
