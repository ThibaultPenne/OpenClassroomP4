Ce CD était son Graal. Le chef de la cellule terroriste en avait confié la garde à Kenny. Il devait contenir quelque chose, des indices, un code, relatifs à l’attaque prévue.
— C’est fini maintenant, dit‑il.
Pour elle, tout au moins. Car en ce qui le concernait, il lui restait encore un long chemin à parcourir.
— Je vais m’assurer que tu sois protégée.
Au lieu de le remercier pour cette attention, elle se jeta sur lui telle une furie.
Elle l’agrippa par les épaules et le secoua, le regard noir.
— Pourquoi ne puis-je pas rentrer chez moi ? hurla-t‑elle.
Elle avait toujours été énergique. Elle était allée à l’école grâce à une bourse sportive, avait été mise sur la touche à cause d’un genou blessé, et avait repris la boucherie de son oncle quand celui-ci avait pris sa retraite.
Reid lui emprisonna les poignets et tenta de l’attirer vers lui pour la soumettre. Plus facile à dire qu’à faire. Il avait face à lui un mètre quatre-vingts de fureur brute.
— Ils vont aller chez-toi, tenta-t‑il d’expliquer pour la ramener à la raison.
Elle gesticula de plus belle pour se libérer, les yeux fixés sur la porte.
— Laisse-moi partir !
Ses bras battaient l’air comme les ailes d’un moulin.
— Lara ?
Il reçut un coup de coude dans le menton, et poussa un juron. Il avait juste besoin de la coincer entre ses bras, mais elle refusait de coopérer.
— Il faut que je retrouve Zak et Nate.
Elle lança en arrière un coup de pied vicieux qui lui toucha le tibia.
— J’ignore qui ils sont, dit‑il, mais il faudra qu’ils se débrouillent seuls.
Combien d’hommes avait‑elle dans sa vie ?
— Tu es fou ? hurla-t‑elle.
Cette fois, son coude se planta dans son torse.
— Ce sont des bébés !
Des bébés.
Le type qui était avec elle au restaurant était sans doute son mari. Un fluide glacé se propagea dans la poitrine de Reid. Il devait avoir grillé quelques neurones. Il connaissait à peine Lara. C’était juste une erreur de parcours commise deux ans plus tôt. Une perte de contrôle temporaire qui n’aurait jamais dû se produire. En quoi cela pouvait‑il le toucher qu’elle se soit mariée depuis ?
Il avait presque réussi à la neutraliser quand tout à coup elle se laissa tomber de tout son poids et le fit tomber au sol avec elle. Mais il était trop rapide pour se laisser désarçonner aussi facilement. La seconde suivante, il était au-dessus d’elle et lui maintenait les poignets au-dessus de la tête. A califourchon sur elle, il avait bien du mal à endiguer le flot de souvenirs brûlants qui lui chauffaient le corps.
Lara s’arc-bouta alors pour le renverser, ce qui n’affaiblit en rien son ardeur.
— S’il arrive quoi que ce soit à Zak et Nate, je te tue, menaça-t‑elle. Tu m’entends ?
Reid avait conscience du galbe de ses hanches sous lui, de ses longues jambes mêlées aux siennes. Les souvenirs se firent plus précis et le submergèrent. Une sorte de paralysie lui envahit les membres et pendant plusieurs secondes il ne put bouger que le cou.
Le temps d’un battement de cœur, il ne ressentit plus rien qu’un désir impérieux.
Bon sang.
Lui qui pensait en avoir fini avec ça.
Puis son corps revint à la vie et une douleur intense lui vrilla l’entrejambe. Lara avait shooté dans l’endroit le plus sensible de son anatomie. Se débarrassant de lui d’un coup de reins, elle plongea vers la porte.


